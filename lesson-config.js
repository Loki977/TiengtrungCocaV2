function createEvenConfig(level, label, description, lessonCount) {
  return {
    level,
    label,
    description,
    splitEvenly: true,
    lessons: Array.from({ length: lessonCount }, (_, index) => ({
      lessonId: index + 1,
      title: `${label.split(" - ")[0]} - Bài ${index + 1}`,
      vocabularyCount: null,
      sentenceCount: 10
    }))
  };
}

export function getLessonConfig(level) {
  const key = String(level || "").toLowerCase();
  return LESSON_CONFIGS[key];
}
